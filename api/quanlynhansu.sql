END$$

CREATE DEFINER=`root`@`localhost` PROCEDURE `TinhLuongThang` (IN `pThang` CHAR(7))   BEGIN
    -- Xóa dữ liệu cũ của tháng này
    DELETE FROM Luong WHERE Thang = pThang;

    -- Tính toán và chèn dữ liệu mới
    INSERT INTO Luong (MaNV, Thang, LuongCB, TheoGio, TangCa, TongLuong)
    SELECT 
        nv.MaNV,
        pThang AS Thang,
        nv.LuongCB,
        SUM(CASE WHEN cc.LoaiCong = 'Công thường' THEN cc.GioLam ELSE 0 END) * (nv.LuongCB / 22 / 8) AS TheoGio,
        SUM(CASE WHEN cc.LoaiCong = 'Tăng ca' THEN cc.GioLam ELSE 0 END) * (nv.LuongCB / 22 / 8 * 1.5) AS TangCa,
        SUM(CASE WHEN cc.LoaiCong IN ('Công thường','Tăng ca') THEN cc.GioLam ELSE 0 END) * (nv.LuongCB / 22 / 8)
        + SUM(CASE WHEN cc.LoaiCong = 'Tăng ca' THEN cc.GioLam ELSE 0 END) * (nv.LuongCB / 22 / 8 * 0.5)
        AS TongLuong
    FROM NhanVien nv
    LEFT JOIN ChamCong cc ON nv.MaNV = cc.MaNV 
        AND DATE_FORMAT(cc.Ngay, '%Y-%m') = pThang
    GROUP BY nv.MaNV;
END$$

