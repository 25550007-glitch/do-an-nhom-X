    )
    SELECT 
        nv.MaNV,
        p_Thang,
        p_Nam,
        nv.LuongCB,
        IFNULL(SUM(cc.GioLam), 0),
        IFNULL(SUM(CASE WHEN cc.LoaiCong = 'Tăng ca' THEN cc.GioLam ELSE 0 END), 0),
        IFNULL(MAX(tmp.Thuong), 0),
        IFNULL(MAX(tmp.PhuCap), 0),
